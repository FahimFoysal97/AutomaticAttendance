package com.foysal.automaticattendance;

public class ItemListCourse {
    private String head;
    private String tail;

    public ItemListCourse(String head, String tail) {
        this.head = head;
        this.tail = tail;
    }

    public String getHead() {
        return head;
    }

    public String getTail() {
        return tail;
    }
}
